class UserContact:
    def __init__(self,
                 name:str,
                 phone_number:str = "",
                 discription:str = "" ):
        self.name = name 
        self.phone_number = phone_number
        self.discription = discription

    def set_phone_number(self, phone_number:str):
        self.phone_number = phone_number

    def set_discription(self, discription:str):
        self.discription = discription
 
    def __str__(self):
        return f'Имя: {self.name}, Номер телефона: {self.phone_number}, Описание контакта: {self.discription}'


class ContactBook:
    def __init__(self):
        self.contacts = []

    def add_contact(self,contact: UserContact):
        self.contacts.append(contact)

    

    def find_contact(self, name):
        found_contacts = []

        for contact in self.contacts:
            if contact.name == name:
                found_contacts.append(contact)
        return self.contacts
    
    def get_contacts(self):
        return self.contacts
    

class ContactBuilder:
    def __init__(self):
        self.current_contact = {}
        self.saved_contacts = {}

    def get_contacts(self, chat_id):
        if chat_id not in self.saved_contacts:
            return []
        return self.saved_contacts[chat_id].get_contacts()

    def add_name(self, chat_id, name):
        self.current_contact[chat_id] = UserContact(name)

    def add_phone_number(self,chat_id, phone_number):
        self.current_contact[chat_id].set_phone_number(phone_number)

    def add_discription(self,chat_id, discriprion):
        self.current_contact[chat_id].set_discription(discriprion)

    def build(self, chat_id):
        current_contact = self.current_contact[chat_id]

        if chat_id not in self.saved_contacts:
            self.saved_contacts[chat_id] = ContactBook()

        self.saved_contacts[chat_id].add_contact(current_contact)

        del self.current_contact[chat_id]
        
    




















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































